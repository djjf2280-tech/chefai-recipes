import 'package:flutter/material.dart';
import '../models/recipe.dart';

class RecipesData extends ChangeNotifier {
  String _searchQuery = '';
  String _selectedCategory = 'Все';

  String get searchQuery => _searchQuery;
  String get selectedCategory => _selectedCategory;

  void setSearch(String q) {
    _searchQuery = q;
    notifyListeners();
  }

  void setCategory(String c) {
    _selectedCategory = c;
    notifyListeners();
  }

  List<String> get categories => [
        'Все', 'Завтрак', 'Обед', 'Ужин',
        'Закуски', 'Суп', 'Десерты', 'Напитки',
      ];

  List<Recipe> get allRecipes => _recipes;

  List<Recipe> get filteredRecipes {
    var list = _recipes;
    if (_selectedCategory != 'Все') {
      list = list.where((r) => r.category == _selectedCategory).toList();
    }
    if (_searchQuery.isNotEmpty) {
      final q = _searchQuery.toLowerCase();
      list = list.where((r) =>
        r.title.toLowerCase().contains(q) ||
        r.description.toLowerCase().contains(q) ||
        r.tags.any((t) => t.toLowerCase().contains(q)) ||
        r.ingredients.any((i) => i.name.toLowerCase().contains(q))
      ).toList();
    }
    return list;
  }

