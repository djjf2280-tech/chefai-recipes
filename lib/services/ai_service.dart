import 'dart:async';
import 'dart:math';
import 'package:flutter/foundation.dart';

/// AI Service - умный кулинарный помощник на правилах + паттерн-матчинг.
/// Работает полностью офлайн, без API ключей.
class AiService extends ChangeNotifier {
  final List<ChatMessage> _messages = [];
  bool _isTyping = false;
  bool _isInitialized = false;

  List<ChatMessage> get messages => List.unmodifiable(_messages);
  bool get isTyping => _isTyping;
  bool get isInitialized => _isInitialized;

  AiService() {
    _init();
  }

  Future<void> _init() async {
    await Future.delayed(const Duration(seconds: 1));
    _isInitialized = true;
    _messages.add(ChatMessage(
      text: '👨‍🍳 Привет! Я ШефАИ — твой персональный кулинарный помощник!\n\n'
          'Я умею:\n'
          '• 🍽️ Подбирать рецепты по ингредиентам\n'
          '• ⏱️ Советовать по технике приготовления\n'
          '• 🔄 Предлагать замены продуктов\n'
          '• 📏 Пересчитывать порции\n'
          '• 💡 Давать кулинарные лайфхаки\n\n'
          'О чём хочешь узнать?',
      isUser: false,
      timestamp: DateTime.now(),
    ));
    notifyListeners();
  }

  Future<void> sendMessage(String text) async {
    if (text.trim().isEmpty) return;

    _messages.add(ChatMessage(
      text: text,
      isUser: true,
      timestamp: DateTime.now(),
    ));
    _isTyping = true;
    notifyListeners();

    // Имитируем "думание" AI
    final thinkTime = 800 + Random().nextInt(1200);
    await Future.delayed(Duration(milliseconds: thinkTime));

    final response = _generateResponse(text.toLowerCase().trim());

    _messages.add(ChatMessage(
      text: response,
      isUser: false,
      timestamp: DateTime.now(),
    ));
    _isTyping = false;
    notifyListeners();
  }

