class AiService extends ChangeNotifier {
  final List<ChatMessage> _messages = [];
  bool _isTyping = false;
  bool _isInitialized = false;
  final List<String> _context = [];

  List<ChatMessage> get messages => List.unmodifiable(_messages);
  bool get isTyping => _isTyping;
  bool get isInitialized => _isInitialized;

  AiService() { _init(); }

  Future<void> _init() async {
    await Future.delayed(const Duration(milliseconds: 400));
    _isInitialized = true;
    _messages.add(ChatMessage(
      text: '👨‍🍳 Приветствую тебя на моей кухне!\n\n'
          'Я — Шеф Максим. 20 лет я варю, жарю и запекаю '
          'в ресторанах Парижа, Токио и Москвы!\n\n'
          'Спрашивай меня про:\n'
          '🍽️ Рецепты любой кухни мира\n'
          '🔥 Техники и секреты приготовления\n'
          '🔄 Замены ингредиентов\n'
          '🌡️ Температуры и время готовки\n'
          '🧂 Специи и приправы\n'
          '💡 Лайфхаки шеф-повара\n\n'
          'Что готовим сегодня? ✨',
      isUser: false,
      timestamp: DateTime.now(),
    ));
    notifyListeners();
  }

  Future<void> sendMessage(String text) async {
    if (text.trim().isEmpty) return;
    _messages.add(ChatMessage(text: text, isUser: true, timestamp: DateTime.now()));
    _context.add(text.toLowerCase());
    if (_context.length > 6) _context.removeAt(0);
    _isTyping = true;
    notifyListeners();

    await Future.delayed(Duration(milliseconds: 600 + Random().nextInt(800)));

    final response = ChefMaxim.respond(text, _context);
    _messages.add(ChatMessage(text: response, isUser: false, timestamp: DateTime.now()));
    _isTyping = false;
    notifyListeners();
  }
}

